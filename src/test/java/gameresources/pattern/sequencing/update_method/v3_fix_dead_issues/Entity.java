package gameresources.pattern.sequencing.update_method.v3_fix_dead_issues;

abstract class Entity {
    private double x;
    private double y;

    public double getX() {
        return x;
    }

    public void setX(double x) {
        this.x = x;
    }

    public double getY() {
        return y;
    }

    public void setY(double y) {
        this.y = y;
    }

    public abstract void update();
}
